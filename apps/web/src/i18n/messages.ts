function deepMerge<T extends Record<string, unknown>>(base: T, extra: Partial<T>): T {
  const out = { ...base } as T;
  for (const k of Object.keys(extra) as (keyof T)[]) {
    const v = extra[k];
    if (v && typeof v === "object" && !Array.isArray(v) && typeof base[k] === "object" && base[k] !== null) {
      out[k] = deepMerge(base[k] as Record<string, unknown>, v as Record<string, unknown>) as T[keyof T];
    } else if (v !== undefined) {
      out[k] = v as T[keyof T];
    }
  }
  return out;
}


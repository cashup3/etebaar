type MsgTree = typeof enTree;

function flatten(prefix: string, node: unknown, out: Record<string, string>) {
  if (node === null || node === undefined) return;
  if (typeof node === "string") {
    const key = prefix.endsWith(".") ? prefix.slice(0, -1) : prefix;
    out[key] = node;
    return;
  }
  if (typeof node === "object" && !Array.isArray(node)) {
    for (const [k, v] of Object.entries(node)) {
      flatten(`${prefix}${k}.`, v, out);
    }
  }
}

function buildFlat(tree: MsgTree): Record<string, string> {
  const o: Record<string, string> = {};
  flatten("", tree as unknown as Record<string, unknown>, o);
  return o;
}

const enFlat = buildFlat(enTree as MsgTree);
const faMerged = deepMerge(
  JSON.parse(JSON.stringify(enTree)) as MsgTree,
  faTree as unknown as Partial<MsgTree>,
) as MsgTree;
const faFlat = buildFlat(faMerged);

export const messageMap: Record<Locale, Record<string, string>> = {
  en: enFlat,
  fa: faFlat,
};

export function translate(locale: Locale, key: string): string {
  return messageMap[locale][key] ?? messageMap.en[key] ?? key;
}
